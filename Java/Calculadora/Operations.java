public class Operations {
    double sum(double num1, double num2) {
        return num1 + num2;
    }

    double minus(double num1, double num2) {
        return num1 - num2;
    }

    double mult(double num1, double num2) {
        return num1 * num2;
    }

    double div(double num1, double num2) {
        return num1 / num2;
    }

}